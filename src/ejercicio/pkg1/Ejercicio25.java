package ejercicio.pkg1;

public class Ejercicio25 {

    public static void main(String[] args) {
       
        double m = 1;
        double n = 2;
        double p = 3;
        double q = 4;
        
        
        double a = m / n * (p + q);
        double b = m / n + 1;
        double c = (m + 1) / n ;
        double d = m + n;
        double f = Math.pow((m + n), 2)  * (p - q);
        
        System.out.println("Primer apartado: " + a);
        System.out.println("Segundo apartado: " + b);
        System.out.println("Tercer apartado: " + c);
        System.out.println("Cuarto apartado: " + d);
        System.out.println("Quinto apartado: " + f);
        
        
        
    }

}